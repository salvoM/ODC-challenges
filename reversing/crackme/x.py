#!/usr/bin/python3
"""
The binary was patched to avoid anti-reversing techniques
and then it was easy to retrieve the data which was then xored with the 
result, producing the key
"""
flag = ""
key1    = "\x19\x83\x89\xd2\x6e\x1f\x84\x1c\x94\x11\x31\x82\xde\x04\xe9\x9b\xf0\xc9\x18\xbb\x82\x51\xaa\xba\x13\x9e\x44\xec\x49\xe5\xad\x49\x01\x86\xab\x39\x6a"
dat     = '\x7f\xef\xe8\xb5\x15\x73\xb4\x6a\xa7\x7d\x48\xdd\xea\x6a\x9d\xaa\x82\xfa\x6e\xe4\xf6\x23\x9b\xd9\x78\xab\x1b\x9b\x16\x96\x9c\x2e\x6f\xb2\xc7\x0c\x17'
assert len(dat) == len(key1)

for i in range(len(key1)):
    flag += chr(ord(key1[i]) ^ ord(dat[i]))
print(flag)